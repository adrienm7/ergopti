def normalize_name(name: str) -> str:
    """Convert an AHK block name to a TOML-safe identifier.

    Lowercases, strips accents, and replaces non-alphanumeric characters with
    underscores.

    Args:
            name: Original AHK block name (e.g. "SFBsReduction", "IÉ").

    Returns:
            Normalised identifier (e.g. "sfbsreduction", "i_e").
    """
    name = name.lower()
    name = unicodedata.normalize("NFD", name)
    name = "".join(c for c in name if unicodedata.category(c) != "Mn")
    name = re.sub(r"[^a-z0-9_]", "_", name)
    name = re.sub(r"_+", "_", name).strip("_")
    return name


def escape_toml_string(text: str) -> str:
    """Escape backslashes and double-quotes for a TOML double-quoted string.

    Args:
            text: Raw string value.

    Returns:
            Escaped string safe to embed between TOML double-quotes.
    """
    text = text.replace("\\", "\\\\")
    text = text.replace('"', '\\"')
    return text


def process_autohotkey_escapes(text: str) -> str:
    """Replace AHK escape sequences with their actual characters.

    Args:
            text: Text containing AHK escape sequences.

    Returns:
            Text with escape sequences expanded.
    """
    replacements: dict[str, str] = {
        '`"': '"',
        "`'": "'",
        "``": "`",
        "`n": "\n",
        "`t": "\t",
        "`r": "\r",
        "`{": "{",
        "`}": "}",
        # Cursor-positioning sequences that must simply be removed
        '""{Left}': '"',
        '"{Left}': "",
        "{Left}": "",
        "{Right}": "",
        "{Up}": "",
        "{Down}": "",
        "{Home}": "",
        "{End}": "",
    }
    for seq, replacement in replacements.items():
        text = text.replace(seq, replacement)
    return text


# ---------------------------------------------------------------------------
# AHK variable substitution & string-concatenation collapsing
# ---------------------------------------------------------------------------

# Map AHK global variable names → their compile-time value as a quoted AHK
# string literal so that they can be inlined before regexes are applied.
_AHK_VARIABLE_SUBSTITUTIONS: list[tuple[str, str]] = [
    # SpaceAroundSymbols is " " when the feature is enabled (the default).
    (r"\bSpaceAroundSymbols\b", '" "'),
    # ScriptInformation["MagicKey"] resolves to the magic-key character ★.
    (r'ScriptInformation\["MagicKey"\]', '"★"'),
]

# Patterns that indicate loop-level or callback-level hotstring registrations
# that cannot be statically resolved; they are skipped silently (no warning).
_UNRESOLVABLE_PATTERNS = re.compile(
    r"\bLetter\b"  # for Letter in StrSplit("abc...")
    r"|\bVowel\b"  # for Vowel in DeadkeyMappingCircumflex
    r"|\bcombo\b"  # combo parameter inside CreateHotstringCombo body
    r"|(?<![A-Za-z])value\b"  # value parameter (function body)
    r"|\bcreatehotstring\b.*\bfrom\b"  # CreateHotstring("", from, to)
    r"|=>.+"  # arrow-function callback (AHK2 syntax)
    r"|\bhotstringhandler\b"  # internal handler call
    r"|\bPersonalInformation\b"  # runtime private-data dictionary
    r"|\bPersonalInformationHotstrings\b"  # runtime private-data collection
    r"|\bGeneratePersonalInformation"  # custom generator function
    r"|\bCreateHotstringCombo"  # custom combo-hotstring functions
    r"|\bCreateDeadkeyHotstring\b"  # custom dead-key hotstring function
    r"|[`]\"",  # AHK2 backtick-escaped double-quote inside a string literal
    re.IGNORECASE,
)

# Collapse adjacent AHK string concatenations: "a" . "b" → "ab".
# AHK2 uses backtick (not backslash) for in-string escaping, so the group
# pattern is simply [^"] (no backslash-escape handling needed here).
_CONCAT_RE = re.compile(r'"([^"]*)"\s*\.\s*"([^"]*)"')


def _collapse_ahk_string_concat(line: str) -> str:
    """Collapse ``"a" . "b"`` AHK concatenations into ``"ab"`` repeatedly.

    Args:
            line: AHK source line, possibly containing string concatenations.

    Returns:
            Line with all adjacent string literals merged.
    """
    prev: Optional[str] = None
    while prev != line:
        prev = line
        line = _CONCAT_RE.sub(
            lambda m: '"' + m.group(1) + m.group(2) + '"', line
        )
    return line


def preprocess_ahk_line(line: str) -> str:
    """Substitute known AHK variables and collapse string concatenations.

    Replaces compile-time global variable references (e.g. ``SpaceAroundSymbols``,
    ``ScriptInformation["MagicKey"]``) with their resolved string-literal values,
    then collapses adjacent ``"a" . "b"`` segments into ``"ab"``.

    Args:
            line: A single logical AHK statement (may be a joined multi-line call).

    Returns:
            The preprocessed line, ready for regex-based hotstring extraction.
    """
    for pattern, replacement in _AHK_VARIABLE_SUBSTITUTIONS:
        line = re.sub(pattern, replacement, line)
    return _collapse_ahk_string_concat(line)


