def _parse_ahk_desc_expr(expr: str) -> str:
    """Resolve an AHK string-concatenation expression into a plain string.

    Replaces ``ScriptInformation["..."]`` references with ``★`` and joins all
    double-quoted fragments.

    Args:
            expr: Raw AHK expression after ``Description:``, e.g.
                    ``"text " . ScriptInformation["MagicKey"] . " suite"``.

    Returns:
            Resolved plain string, e.g. ``"text ★ suite"``.
    """
    # Split on the AHK concatenation operator ' . ' (always surrounded by spaces).
    # Using \s+ (not \s*) avoids splitting on literal dots inside quoted strings
    # (e.g. "ê. = u." or "$eq.not$").
    tokens = re.split(r"\s+\.\s+", expr.strip().rstrip(","))
    parts: list[str] = []
    for tok in tokens:
        tok = tok.strip()
        if tok.startswith('"') and tok.endswith('"'):
            # Plain quoted literal — unescape AHK backtick sequences
            parts.append(process_autohotkey_escapes(tok[1:-1]))
        elif "ScriptInformation" in tok:
            parts.append("★")
        # else: skip things like numbers, booleans, etc.
    return "".join(parts)


def parse_ahk_descriptions(content: str) -> dict[str, dict[str, str]]:
    """Extract per-section descriptions from the ``Features`` map in *content*.

    Scans the AHK source for ``"SubName", { ... Description: ... }`` blocks
    and records the resolved description text for each ``(category, sub)`` pair.

    Args:
            content: Full AHK file content.

    Returns:
            ``{AHK_category_name: {AHK_sub_name: description_string}}``
    """
    # Locate the Features map block
    features_start = content.find("global Features := Map(")
    if features_start < 0:
        return {}

    # The Features map is followed by the first top-level if/hotstring block.
    # We only scan the initial declarations section.
    first_if = re.search(
        r"^\s*if\s+Features\[", content[features_start:], re.MULTILINE
    )
    features_text = (
        content[features_start : features_start + first_if.start()]
        if first_if
        else content[features_start : features_start + 20_000]
    )

    result: dict[str, dict[str, str]] = {}

    # Regexes to track context
    cat_re = re.compile(r'"([A-Za-z]+)",\s*Map\(')
    sub_re = re.compile(r'"([^"]+)",\s*\{')
    desc_re = re.compile(r"Description:\s*(.*)")

    lines = features_text.splitlines()
    current_cat: Optional[str] = None
    current_sub: Optional[str] = None
    i = 0

    while i < len(lines):
        line = lines[i].strip()

        cm = cat_re.search(line)
        if cm and cm.group(1) != "__Order":
            current_cat = cm.group(1)
            current_sub = None

        sm = sub_re.search(line)
        if sm:
            cand = sm.group(1)
            if cand not in ("__Order",) and not cand.startswith("-"):
                current_sub = cand

        dm = desc_re.search(line)
        if dm and current_cat and current_sub:
            desc_expr = dm.group(1).strip()
            # Collect continuation lines when the expression is incomplete:
            # either it ends with the concatenation operator '.' or it ends
            # with an open bracket (e.g. ScriptInformation[ split across lines).
            while (
                desc_expr.rstrip(",").rstrip().endswith(".")
                or desc_expr.rstrip().endswith("[")
            ) and i + 1 < len(lines):
                i += 1
                desc_expr += " " + lines[i].strip()
            desc = _parse_ahk_desc_expr(desc_expr)
            if desc:
                result.setdefault(current_cat, {})[current_sub] = desc

        i += 1

    # Also scan standalone  Features["Cat"] := Map(...)  declarations that
    # appear outside the main global map (e.g. section 2 of the private file).
    standalone_cat_re = re.compile(r'Features\["([A-Za-z]+)"\]\s*:=\s*Map\(')
    for standalone_m in standalone_cat_re.finditer(content):
        cat_name = standalone_m.group(1)
        block_text = content[standalone_m.end() :]
        close_m = re.search(r"^\)", block_text, re.MULTILINE)
        if close_m:
            block_text = block_text[: close_m.start()]
        current_sub = None
        for bline in block_text.splitlines():
            bline_s = bline.strip()
            sm2 = sub_re.search(bline_s)
            if sm2:
                cand = sm2.group(1)
                if cand not in ("__Order",) and not cand.startswith("-"):
                    current_sub = cand
            dm = desc_re.search(bline_s)
            if dm and current_sub:
                desc = _parse_ahk_desc_expr(dm.group(1).strip())
                if desc:
                    result.setdefault(cat_name, {})[current_sub] = desc

    return result


def parse_ahk_orders(content: str) -> dict[str, list[str]]:
    """Extract the ``__Order`` list for every category in the AHK Features map.

    Separators (``"-"``) are preserved as ``"-"``; all other entries are
    normalized with :func:`normalize_name` to match the TOML section keys.
    Sections not mentioned in ``__Order`` but present in the data will be
    appended (in their original position) by the caller.

    Args:
        content: Full AHK file content.

    Returns:
        ``{category_name: [normalized_sub, "-", ...]}``
    """
    features_start = content.find("global Features := Map(")
    if features_start < 0:
        return {}

    first_if = re.search(
        r"^\s*if\s+Features\[", content[features_start:], re.MULTILINE
    )
    features_text = (
        content[features_start : features_start + first_if.start()]
        if first_if
        else content[features_start : features_start + 20_000]
    )

    result: dict[str, list[str]] = {}
    cat_re = re.compile(r'"([A-Za-z]+)",\s*Map\(')
    order_start_re = re.compile(r'"__Order"\s*,\s*\[')

    lines = features_text.splitlines()
    current_cat: Optional[str] = None
    i = 0

    while i < len(lines):
        line = lines[i]

        cm = cat_re.search(line)
        if cm and cm.group(1) != "__Order":
            current_cat = cm.group(1)

        if current_cat and order_start_re.search(line):
            # Collect the full array text — may span multiple lines
            array_text = ""
            # Take the portion after "["
            bracket_pos = line.find("[")
            array_text += line[bracket_pos + 1 :]
            # Consume continuation lines until the closing "]"
            while "]" not in array_text and i + 1 < len(lines):
                i += 1
                array_text += " " + lines[i]
            # Strip everything after the closing "]"
            close = array_text.find("]")
            if close >= 0:
                array_text = array_text[:close]
            # Extract all quoted items
            items: list[str] = re.findall(r'"([^"]+)"', array_text)
            normalized: list[str] = [
                "-" if item == "-" else normalize_name(item) for item in items
            ]
            result[current_cat] = normalized

        i += 1

    # Also scan standalone  Features["Cat"] := Map(...)  declarations.
    standalone_cat_re = re.compile(r'Features\["([A-Za-z]+)"\]\s*:=\s*Map\(')
    order_start_re_local = re.compile(r'"__Order"\s*,\s*\[')
    for standalone_m in standalone_cat_re.finditer(content):
        cat_name = standalone_m.group(1)
        block_text = content[standalone_m.end() :]
        close_m = re.search(r"^\)", block_text, re.MULTILINE)
        if close_m:
            block_text = block_text[: close_m.start()]
        for j, bline in enumerate(block_text.splitlines()):
            if order_start_re_local.search(bline):
                bracket_pos = bline.find("[")
                array_text = bline[bracket_pos + 1 :]
                remaining = block_text.splitlines()[j + 1 :]
                k = 0
                while "]" not in array_text and k < len(remaining):
                    array_text += " " + remaining[k]
                    k += 1
                close_bracket = array_text.find("]")
                if close_bracket >= 0:
                    array_text = array_text[:close_bracket]
                items: list[str] = re.findall(r'"([^"]+)"', array_text)
                normalized: list[str] = [
                    "-" if item == "-" else normalize_name(item)
                    for item in items
                ]
                result[cat_name] = normalized
                break

    return result


def parse_ahk_top_level_order(content: str) -> list[str]:
    """Extract the top-level ``__Order`` list from the AHK Features map.

    Returns the normalized category names in the order defined at the root of
    ``global Features := Map(...)`` (i.e. the order of the main categories,
    not the sub-sections within each category).

    Args:
            content: Full AHK file content.

    Returns:
            List of normalized category names in the defined order.  Separators
            (``"-"``) are excluded.
    """
    features_start = content.find("global Features := Map(")
    if features_start < 0:
        return []

    first_if = re.search(
        r"^\s*if\s+Features\[", content[features_start:], re.MULTILINE
    )
    features_text = (
        content[features_start : features_start + first_if.start()]
        if first_if
        else content[features_start : features_start + 20_000]
    )

    cat_re = re.compile(r'"([A-Za-z]+)",\s*Map\(')
    order_start_re = re.compile(r'"__Order"\s*,\s*\[')

    lines = features_text.splitlines()
    i = 0
    while i < len(lines):
        line = lines[i]
        # Stop as soon as we reach the first category Map – the top-level
        # __Order must come before any sub-Map definition.
        if cat_re.search(line):
            break
        order_m = order_start_re.search(line)
        if order_m:
            bracket_pos = line.find("[")
            array_text = line[bracket_pos + 1 :]
            while "]" not in array_text and i + 1 < len(lines):
                i += 1
                array_text += " " + lines[i]
            close = array_text.find("]")
            if close >= 0:
                array_text = array_text[:close]
            items: list[str] = re.findall(r'"([^"]+)"', array_text)
            return [normalize_name(it) for it in items if it != "-"]
        i += 1
    return []


def parse_ahk_circumflex_map(content: str) -> dict[str, str]:
    """Parse the ``DeadkeyMappingCircumflex`` global map from the AHK source.

    The map contains alternating key / value string pairs; each pair
    ``key, value`` produces one entry ``{key: value}`` in the returned dict.
    Line comments (``; …``) are stripped before extraction so they cannot
    interfere with the quoted-string scan.

    Args:
            content: Full AHK file content.

    Returns:
            Mapping of dead-key character to replacement character.  Excludes
            nothing — callers decide which entries to use.
    """
    marker = "global DeadkeyMappingCircumflex := Map("
    start = content.find(marker)
    if start < 0:
        logger.warning("DeadkeyMappingCircumflex not found in AHK source.")
        return {}

    # Walk forward to find the matching closing paren.
    idx = start + len(marker)
    depth = 1
    while idx < len(content) and depth > 0:
        c = content[idx]
        if c == "(":
            depth += 1
        elif c == ")":
            depth -= 1
        idx += 1
    map_body = content[start + len(marker) : idx - 1]

    # Strip inline comments so semicolons inside them can't interfere.
    # A naive line.find(";") would incorrectly truncate at the ";" key entry,
    # so we track whether we are inside a double-quoted string first.
    def _strip_comment(line: str) -> str:
        in_str = False
        for i, ch in enumerate(line):
            if ch == '"':
                in_str = not in_str
            elif ch == ";" and not in_str:
                return line[:i]
        return line

    clean_lines: list[str] = [
        _strip_comment(line) for line in map_body.splitlines()
    ]
    clean_body = "\n".join(clean_lines)

    # Extract all double-quoted literal strings.  AHK2 uses backtick for
    # in-string escaping, so [^"] is safe as the non-capturing inner pattern.
    strings = re.findall(r'"([^"]*)"', clean_body)

    # Pair up: even index = key, odd index = value.
    result: dict[str, str] = {}
    for i in range(0, len(strings) - 1, 2):
        result[strings[i]] = strings[i + 1]
    return result


