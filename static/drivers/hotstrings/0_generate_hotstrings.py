def _extract_hotstring_from_line(
    line: str,
) -> tuple[Optional[str], Optional[str], bool, bool, bool, bool, bool]:
    """Parse one (possibly multi-line-joined) AHK call and return its fields.

    Applies :func:`preprocess_ahk_line` first so that known AHK global
    variables (``SpaceAroundSymbols``, ``ScriptInformation["MagicKey"]``) are
    resolved and adjacent string literals are merged before the regexes run.

    Args:
            line: A single logical AHK statement, with whitespace normalised.

    Returns:
            ``(trigger, output, is_word, auto_expand, case_sensitive,
            final_result, strict_case)`` where *trigger* and *output* are
            ``None`` when no hotstring was found. ``final_result`` is extracted
            from an explicit ``Map("FinalResult", True/False)`` argument if
            present, otherwise derived automatically from the output via
            :func:`_needs_final_result`. ``strict_case`` is ``True`` when the
            original AHK flags contained a ``C`` modifier on a plain
            ``CreateHotstring`` call — this indicates a case-sensitive match
            that the AHK loader must reinstate by re-adding ``C`` to its
            generated flag string (``CreateCaseSensitiveHotstrings`` already
            emits per-variant hotstrings with their own ``C`` flag, so this
            field is only meaningful when *case_sensitive* is ``False``).
    """
    # Resolve variables and collapse string concatenations upfront.
    line = preprocess_ahk_line(line)

    case_sensitive = "CreateCaseSensitiveHotstrings" in line

    # Try from most-specific to most-generic.
    patterns: list[str] = [
        # Standard CreateHotstring / CreateCaseSensitiveHotstrings.
        (
            r"(?:CreateCaseSensitiveHotstrings|CreateHotstring)\s*\(\s*"
            r'"(?P<opts>[^"]*)",\s*'
            r'"(?P<trig>(?:[^"`\\]|\\.|`.|"")*)",\s*'
            r'"(?P<out>(?:[^"`\\]|\\.|`.|"")*)",?\s*'
        ),
        # Hotstring() legacy format
        (
            r"Hotstrings?\s*\("
            r'(?:\s*"(?P<opts>[^"]*)",)?'
            r'\s*"(?P<trig>[^"]+?)"'
            r'\s*,\s*"(?P<out>[^"]+?)"'
            r"\s*\)"
        ),
    ]

    for pat in patterns:
        m = re.search(pat, line, re.DOTALL)
        if not m:
            continue
        opts = m.group("opts") or ""
        trigger = process_autohotkey_escapes(m.group("trig"))
        output = process_autohotkey_escapes(m.group("out"))
        auto_expand = "*" in opts
        is_word = "?" not in opts
        # ``C`` on a plain ``CreateHotstring`` call forces exact-case matching.
        # We only track it when the call is NOT CreateCaseSensitiveHotstrings,
        # since the latter already synthesises per-variant hotstrings, each
        # carrying its own ``C`` flag — re-capturing ``C`` there would be
        # redundant and would falsely collapse the multi-variant expansion.
        strict_case = ("C" in opts) and not case_sensitive

        # --- NOUVELLE LOGIQUE POUR FINAL_RESULT ---
        # On cherche un argument "FinalResult", True|False|1|0 dans la suite de la ligne
        explicit_fr_match = re.search(
            r'"FinalResult"\s*,\s*(true|false|1|0)\b',
            line[m.end() :],
            re.IGNORECASE,
        )

        if explicit_fr_match:
            val = explicit_fr_match.group(1).lower()
            final_result = val in ("true", "1")
        else:
            final_result = _needs_final_result(output)
        # ------------------------------------------

        return (
            trigger,
            output,
            is_word,
            auto_expand,
            case_sensitive,
            final_result,
            strict_case,
        )

    return None, None, False, False, False, False, False


def _strip_ahk_inline_comment(line: str) -> str:
    """Strip an AHK inline comment (``; ...``) from *line*.

    Respects double-quoted string literals so that semicolons inside quotes
    are not treated as comment starters.  AHK backtick escapes (`` `x ``) are
    handled inside strings.

    Args:
            line: A single AHK statement line.

    Returns:
            The line with the inline comment removed, stripped of trailing
            whitespace.
    """
    in_string = False
    i = 0
    while i < len(line):
        ch = line[i]
        if ch == "`" and in_string:
            # AHK backtick escape: skip the next character unconditionally
            i += 2
            continue
        if ch == '"':
            in_string = not in_string
        elif ch == ";" and not in_string:
            return line[:i].rstrip()
        i += 1
    return line


def _extract_inline_comment(line: str) -> str:
    """Return the AHK inline comment (without the leading ``;``) from *line*.

    Uses the same string-literal-aware scan as :func:`_strip_ahk_inline_comment`
    so semicolons inside quoted strings are not misread as comment starters.

    Args:
            line: A single AHK statement line (may already be stripped).

    Returns:
            The trimmed comment text, or an empty string when there is no
            inline comment.
    """
    in_string = False
    i = 0
    while i < len(line):
        ch = line[i]
        if ch == "`" and in_string:
            i += 2
            continue
        if ch == '"':
            in_string = not in_string
        elif ch == ";" and not in_string:
            return line[i + 1 :].strip()
        i += 1
    return ""


_AHK1_HOTSTRING_HEADER_RE = re.compile(r":([^:\n]*):([^:\n]+)::\s*\{")


def _extract_send_instant_output(block: str) -> str:
    """Extract and normalise the output string from a ``SendInstant(...)`` call.

    Handles both single-line strings and multi-line AHK2 continuation-section
    strings (``"\\n(\\n...\\n)"``) .  Double-quote escape (``""`` → ``"``) and
    backtick escapes are applied to the extracted content.

    Args:
        block: Body text of a ``:*:trigger:: { ... }`` block (between the
               braces), with no surrounding braces.

    Returns:
        Normalised output text, or empty string if no ``SendInstant`` found.
    """
    # Single-line: SendInstant("text here")
    single_m = re.search(r'SendInstant\("([^"\n]*)"\)', block)
    if single_m:
        return process_autohotkey_escapes(single_m.group(1))

    # Multi-line continuation section: SendInstant("\n(\n...\n)"\n)
    # The string literal ends with )" where ) closes the continuation section
    # and " closes the string.
    multi_m = re.search(r'SendInstant\("\s*\n(.*?)\)"', block, re.DOTALL)
    if not multi_m:
        return ""

    raw = multi_m.group(1)

    # Strip the AHK2 continuation-section ( / ) marker lines if present.
    # The opening ( is the first non-empty line; the closing ) was consumed by
    # the )" in the regex, so only the ( line needs to be stripped here.
    lines = raw.split("\n")
    while lines and re.match(r"^\s*\(\s*$", lines[0]):
        lines.pop(0)

    # Dedent: remove common leading whitespace from non-empty lines.
    non_empty = [li for li in lines if li.strip()]
    if non_empty:
        min_indent = min(len(li) - len(li.lstrip()) for li in non_empty)
        lines = [
            li[min_indent:] if len(li) >= min_indent else li for li in lines
        ]

    content = "\n".join(lines).strip()

    # AHK2 in-string double-quote escape inside continuation sections: "" → "
    content = content.replace('""', '"')

    # Apply remaining backtick escapes.
    return process_autohotkey_escapes(content)


def _extract_legacy_hotstrings(
    body: str,
) -> list[tuple[str, str, bool, bool, bool, bool, bool]]:
    """Parse AHK1-style ``:*:trigger:: { SendInstant(...) }`` hotstrings.

    These use the legacy AHK label syntax rather than ``CreateHotstring()``.
    The trigger options (e.g. ``*`` for auto-expand) are parsed from the
    colon-prefix.  The output text is extracted from ``SendInstant()`` and
    optional trailing key tokens are appended from ``SendFinalResult()``.

    Args:
        body: Raw body text of a ``Features[...][...]`` block.

    Returns:
        List of ``(trigger, output, is_word, auto_expand, case_sensitive,
        final_result, strict_case)`` 7-tuples, one per discovered legacy
        hotstring.  ``strict_case`` mirrors the ``_extract_hotstring_from_line``
        contract but is always ``False`` here — legacy entries are funnelled
        through ``CreateCaseSensitiveHotstrings`` whose per-variant synthesis
        already handles case, so re-flagging ``C`` would be redundant.
    """
    results: list[tuple[str, str, bool, bool, bool, bool, bool]] = []
    pos = 0
    while pos < len(body):
        header_m = _AHK1_HOTSTRING_HEADER_RE.search(body, pos)
        if not header_m:
            break

        opts_str = header_m.group(1)  # e.g. "*" or "*C"
        trigger = header_m.group(2).strip()

        # Walk forward from the opening brace, counting balanced braces.
        # Braces inside string literals (between pairs of ") are ignored so
        # that AHK key tokens like {Left} do not confuse the depth counter.
        brace_start = header_m.end()
        depth = 1
        idx = brace_start
        in_str = False
        while idx < len(body) and depth > 0:
            ch = body[idx]
            if ch == "`" and in_str:
                idx += 2  # AHK backtick escape – skip next char
                continue
            if ch == '"':
                in_str = not in_str
            elif not in_str:
                if ch == "{":
                    depth += 1
                elif ch == "}":
                    depth -= 1
            idx += 1
        block = body[brace_start : idx - 1]
        pos = idx

        auto_expand = "*" in opts_str
        is_word = True
        # Mirror the convention used by CreateHotstring vs
        # CreateCaseSensitiveHotstrings: the tuple element means "was this
        # extracted from the CaseSensitive variant?" which entries_to_toml_lines
        # then inverts (str(not case_sensitive)) when writing is_case_sensitive.
        # AHK1 `:C:` option = case-sensitive; absence = case-insensitive (False).
        is_case_sensitive = "C" in opts_str

        output = _extract_send_instant_output(block)
        if not output:
            continue

        # Append key tokens from an optional SendFinalResult("{...}") call.
        final_m = re.search(r'SendFinalResult\("([^"]+)"\)', block)
        if final_m:
            output += final_m.group(1)

        results.append(
            (
                trigger,
                output,
                is_word,
                auto_expand,
                is_case_sensitive,
                _needs_final_result(output),
                False,
            )
        )

    return results


_COMMENTED_OUT_RE = re.compile(
    r"^\s*;+\s*(?:CreateHotstring|CreateCaseSensitiveHotstrings)\s*\("
)


def parse_body(
    body: str,
    *,
    skip_identity: bool = True,
) -> list[tuple[str, str, bool, bool, bool, bool, str, bool, bool]]:
    """Extract all hotstring entries from a block body.

    Section-header comments (``; === Name ===``) are ignored – every entry
    just goes into a flat list.  Duplicate triggers within the same body are
    silently dropped (first occurrence wins).

    In addition to active ``CreateHotstring`` calls, single-line
    **commented-out** hotstrings are also captured (and flagged as disabled
    in the returned tuple) so that downstream callers can carry them over to
    the TOML files.  Trailing ``; …`` comments on an active hotstring line
    are preserved as well.

    Args:
            body: Raw body text of a ``Features[...][...]`` block.
            skip_identity: When ``True`` (default), non-auto-expand hotstrings
                    where trigger equals output are dropped (they are genuine
                    no-ops: they need an end-key to fire and would just retype
                    the trigger).  Auto-expand (``*``) identity hotstrings are
                    **always** kept regardless of this flag: they are guard
                    entries that prevent shorter triggers from firing inside a
                    longer word (e.g. ``prochaine`` blocks shorter SFB
                    corrections that would otherwise match inside it).  Pass
                    ``False`` to also keep non-auto-expand identity entries
                    (used for the ``Repeat`` section).

    Returns:
            Deduplicated list of
            ``(trigger, output, is_word, auto_expand, case_sensitive,
            final_result, comment, is_disabled, strict_case)`` 9-tuples where
            ``comment`` is the trailing ``; …`` note (or empty string),
            ``is_disabled`` marks hotstrings that were commented out in
            the AHK source, and ``strict_case`` forwards the ``C`` flag bit
            from :func:`_extract_hotstring_from_line` so downstream emitters
            can re-apply it to the generated TOML.
    """
    entries: list[tuple[str, str, bool, bool, bool, bool, str, bool, bool]] = []
    seen: set[str] = set()

    # First pass: extract AHK1-style :*:trigger:: { SendInstant(...) } blocks
    # before the line-by-line scan so those triggers are registered in `seen`
    # and their multi-line bodies do not confuse the CreateHotstring parser.
    for legacy in _extract_legacy_hotstrings(body):
        trig, out, iw, ae, cs, fr, sc = legacy
        if skip_identity and trig == out:
            continue
        if trig not in seen:
            seen.add(trig)
            entries.append((trig, out, iw, ae, cs, fr, "", False, sc))

    lines = body.split("\n")
    i = 0

    while i < len(lines):
        raw_line = lines[i]
        line = raw_line.strip()
        if not line:
            i += 1
            continue

        # Handle single-line commented-out hotstrings (e.g. ``; CreateHotstring(...)``).
        # Multi-line commented-out blocks are not supported: in practice every
        # disabled entry in the AHK source fits on one line.
        if _COMMENTED_OUT_RE.match(raw_line):
            uncommented = re.sub(r"^\s*;+\s*", "", line, count=1)
            disabled_reason = _extract_inline_comment(uncommented)
            code_part = _strip_ahk_inline_comment(uncommented)
            (
                trigger,
                output,
                is_word,
                auto_expand,
                case_sensitive,
                final_result,
                strict_case,
            ) = _extract_hotstring_from_line(code_part)
            if trigger and output and trigger not in seen:
                seen.add(trigger)
                entries.append(
                    (
                        trigger,
                        output,
                        is_word,
                        auto_expand,
                        case_sensitive,
                        final_result,
                        disabled_reason,
                        True,
                        strict_case,
                    )
                )
            i += 1
            continue

        if line.startswith(";"):
            i += 1
            continue

        # Join continuation lines for multi-line calls — also covers bare
        # ``Hotstring(`` (low-level callback form with no ``Create`` prefix).
        _is_open_call = (
            "CreateCaseSensitiveHotstrings(" in line
            or "CreateHotstring(" in line
            or bool(re.search(r"(?<![A-Za-z])Hotstring\s*\(", line))
        )
        # Track the last source line that contributed to the joined statement
        # so we can extract the trailing ``; …`` comment from that specific
        # line (the comment lives on the line carrying the closing parenthesis).
        last_contrib_i = i
        if _is_open_call and not _strip_ahk_inline_comment(line).endswith(")"):
            j = i + 1
            while j < len(lines) and not _strip_ahk_inline_comment(
                line
            ).endswith(")"):
                next_fragment = lines[j].strip()
                # Ignore standalone comment lines; they corrupt the joined expression.
                if not next_fragment.startswith(";"):
                    # Strip inline comments before joining so that embedded
                    # "; comment" text does not mask the closing ")" in the
                    # loop condition check.
                    stripped_fragment = _strip_ahk_inline_comment(next_fragment)
                    if stripped_fragment:
                        line += " " + stripped_fragment
                        last_contrib_i = j
                j += 1
            i = j
        else:
            i += 1

        tail_comment = _extract_inline_comment(lines[last_contrib_i])

        (
            trigger,
            output,
            is_word,
            auto_expand,
            case_sensitive,
            final_result,
            strict_case,
        ) = _extract_hotstring_from_line(line)

        if trigger and output:
            # Skip non-auto-expand identity hotstrings (trigger == output)
            # by default: without the ``*`` flag they would need an end-key
            # to fire and, upon firing, would simply retype the trigger —
            # a genuine no-op in Hammerspoon.
            # Auto-expand (``*``) identity hotstrings are guard entries that
            # prevent shorter triggers from firing inside a longer word
            # (e.g. ``prochaine`` blocks shorter SFB-correction triggers that
            # would otherwise match inside it).  They must always be kept,
            # regardless of the section.
            if skip_identity and trigger == output and not auto_expand:
                pass
            elif trigger not in seen:
                seen.add(trigger)
                entries.append(
                    (
                        trigger,
                        output,
                        is_word,
                        auto_expand,
                        case_sensitive,
                        final_result,
                        tail_comment,
                        False,
                        strict_case,
                    )
                )
        elif any(
            kw in line
            for kw in (
                "CreateHotstring",
                "CreateCaseSensitiveHotstrings",
                "Hotstring",
            )
        ):
            # Silently skip patterns that are inherently unresolvable at parse
            # time (loop variables, callback lambdas, etc.).
            if not _UNRESOLVABLE_PATTERNS.search(line):
                logger.warning(
                    "Failed to parse: %s",
                    (line[:120] + "...") if len(line) > 120 else line,
                )

    return entries


