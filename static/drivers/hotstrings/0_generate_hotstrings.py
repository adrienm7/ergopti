def _extract_brace_body(content: str, match_end: int) -> str:
    """Return the text between the braces of a block whose opening brace
    was consumed at *match_end*.

    Args:
            content: Full file content.
            match_end: Index just past the opening ``{``.

    Returns:
            Body text (without surrounding braces), or empty string on failure.
    """
    idx = match_end
    depth = 1
    while idx < len(content) and depth > 0:
        if content[idx] == "{":
            depth += 1
        elif content[idx] == "}":
            depth -= 1
        idx += 1
    return content[match_end : idx - 1] if depth == 0 else ""


def find_all_blocks(content: str) -> dict[str, dict[str, str]]:
    """Discover every ``Features["Cat"]["Sub"]`` block in *content*.

    Multiple occurrences of the same ``(cat, sub)`` pair are concatenated so
    that split blocks (e.g. when a condition references two features) are
    merged transparently.

    Args:
            content: Full AHK file content.

    Returns:
            ``{category: {sub_name: body_text}}`` – names in their original casing.
    """
    # Match lines like:
    #   if Features["Cat"]["Sub"].Enabled {
    #   if Features["Cat"]["Sub"] {
    #   if Features["Cat"]["Sub"].Enabled and Features["X"]["Y"].Enabled {
    pattern = re.compile(
        r"(?:^|(?<=\n))\s*(?:} else )?if\s+"
        r'Features\["(?P<cat>[^"]+)"\]\["(?P<sub>[^"]+)"\]'
        r"(?:\.Enabled)?\b[^{]*\{",
    )

    result: dict[str, dict[str, str]] = {}
    for m in pattern.finditer(content):
        cat = m.group("cat")
        sub = m.group("sub")
        body = _extract_brace_body(content, m.end())
        if not body.strip():
            continue
        result.setdefault(cat, {}).setdefault(sub, "")
        result[cat][sub] += ("\n" if result[cat][sub] else "") + body

    return result


