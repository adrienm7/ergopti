# TOML rendering
# ---------------------------------------------------------------------------


def entries_to_toml_lines(
    entries: list[tuple[str, str, bool, bool, bool]],
) -> list[str]:
    """Render a list of hotstring entries as TOML assignment lines.

    Args:
            entries: Hotstring tuples produced by :func:`parse_body`.

    Returns:
            One TOML line per entry.
    """
    lines = []
    for trigger, output, is_word, auto_expand, case_sensitive in entries:
        t = escape_toml_string(trigger)
        o = escape_toml_string(output)
        lines.append(
            f'"{t}" = {{ output = "{o}"'
            f", is_word = {str(is_word).lower()}"
            f", auto_expand = {str(auto_expand).lower()}"
            f", is_case_sensitive = {str(not case_sensitive).lower()} }}"
        )
    return lines


def write_category_toml(
    category: str,
    sub_blocks: dict[str, list[tuple[str, str, bool, bool, bool]]],
    section_descriptions: Optional[dict[str, str]] = None,
) -> int:
    """Write one TOML file for *category* and return the total entry count.

    The filename is :func:`normalize_name` applied to *category*.
    Each sub-block becomes a ``[[sub_name]]`` array-of-tables section.
    A leading ``[_meta]`` table carries the human-readable descriptions used
    by the Hammerspoon menu.

    Args:
            category: Original AHK category name (e.g. ``"MagicKey"``).
            sub_blocks: Mapping of sub-block names to their hotstring entries.
            section_descriptions: Optional ``{AHK_sub_name: description}`` mapping
                    extracted from the AHK Features map.

    Returns:
            Total number of entries written.
    """
    filename = normalize_name(category) + ".toml"
    output_file = OUTPUT_DIR / filename

    lines = TOML_HEADER.copy()

    # ── [_meta] block ────────────────────────────────────────────────────────
    file_desc = FILE_DESCRIPTIONS.get(category, "")
    lines.append("[_meta]")
    lines.append(f'description = "{escape_toml_string(file_desc)}"')
    lines.append("")

    if section_descriptions:
        lines.append("[_meta.sections]")
        for sub_name in sub_blocks:
            norm = normalize_name(sub_name)
            desc = section_descriptions.get(sub_name, "")
            if desc:
                lines.append(f'{norm} = "{escape_toml_string(desc)}"')
        lines.append("")

    # ── section data ─────────────────────────────────────────────────────────
    total = 0
    for sub_name, entries in sub_blocks.items():
        if not entries:
            continue
        lines.append(f"[[{normalize_name(sub_name)}]]")
        lines.extend(entries_to_toml_lines(entries))
        lines.append("")
        total += len(entries)

    if total == 0:
        logger.warning("Skipping %s – no entries found", filename)
        return 0

    output_file.write_text("\n".join(lines), encoding="utf-8")
    logger.info("Written %s (%d entries)", filename, total)
    return total


