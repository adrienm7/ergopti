def _comment_entries() -> list[tuple[str, str, bool, bool, bool, bool, str, bool]]:
    r"""Return entries for the Comment roll: ``\"`` ➜ ``/*`` and ``"\`` ➜ ``*/``.

    The AHK source uses backtick-escaped double-quotes inside the string
    literals (``\`"`` and `` `"\``).  These sequences match
    ``_UNRESOLVABLE_PATTERNS`` and are silently skipped by the parser,
    so they must be hand-crafted.
    """
    return [
        ('\\"', "/*", False, True, False, _needs_final_result("/*"), "", False),
        ('"\\', "*/", False, True, False, _needs_final_result("*/"), "", False),
    ]


# Vowel keys following ê that use is_word=False so the hotstring fires
# mid-word (e.g. "coêut" → "coût").  Non-vowel keys keep is_word=True.
_ECIRCUMFLEX_VOWEL_KEYS: frozenset[str] = frozenset("aAiIoOuUàÀèÈéÉêÊ")


def _deadkey_ecircumflex_entries(
    circumflex_map: dict[str, str],
) -> list[tuple[str, str, bool, bool, bool, bool, str, bool]]:
    """Generate hotstring entries for all ``ê``+key combinations.

    Iterates the full ``DeadkeyMappingCircumflex`` and produces one
    ``(trigger, output, is_word, auto_expand, case_sensitive)`` tuple per
    entry.  Excluded keys:

    - ``"e"`` / ``"E"``: handled by the dedicated ``ECircumflexE`` section
      (``êe`` ➜ ``œ``).
    - ``"t"`` / ``"T"``: reserved so that ``être`` can be typed normally.

    Since both the vowel shortcuts (``CreateCaseSensitiveHotstrings``) and the
    remaining dead-key shortcuts (``CreateDeadkeyHotstring``) use the AHK ``C``
    flag, each (key, value) pair from the map is emitted as a distinct
    case-sensitive entry.

    ``is_word`` behaviour depends on the following key:

    - **Vowel keys** (``_ECIRCUMFLEX_VOWEL_KEYS``): ``is_word=False`` so the
      hotstring fires even mid-word, e.g. ``coêut`` ➜ ``coût``.
    - **Non-vowel keys**: ``is_word=True`` mirrors the AHK
      ``ShouldActivateDeadkey`` logic – the combination only fires at a word
      boundary, preventing mid-word false positives like ``mêm`` in ``même``.

    Args:
            circumflex_map: Parsed ``{key: value}`` dict from the AHK source.

    Returns:
            Ordered list of hotstring entry tuples.
    """
    excluded = {"e", "E", "t", "T"}
    return [
        (
            f"ê{key}",
            value,
            key not in _ECIRCUMFLEX_VOWEL_KEYS,
            True,
            False,
            _needs_final_result(value),
            "",
            False,
        )
        for key, value in circumflex_map.items()
        if key not in excluded
    ]


def _hashtag_quote_entries() -> list[tuple[str, str, bool, bool, bool, bool, str, bool]]:
    """Return entries for the HashtagQuote roll: (# ➜ (" and [# ➜ [\'.

    This roll is implemented via a ``#HotIf`` block in AHK (not a standard
    ``CreateHotstring`` call) and therefore cannot be parsed automatically.
    When the last character sent is ``(`` or ``[``, pressing the AltGr-mapped
    ``#`` key outputs a double-quote instead.
    """
    return [
        ("(#", '("', False, True, False, _needs_final_result('("'), "", False),
        ("[#", '["', False, True, False, _needs_final_result('["'), "", False),
    ]


def _chevron_equal_entries() -> list[tuple[str, str, bool, bool, bool, bool, str, bool]]:
    """Return entries for the ChevronEqual roll: <% ➜ <= and >% ➜ >=.

    This roll is also implemented via a ``#HotIf`` block in AHK and is not
    captured by the standard block parser.  When the last character sent is
    ``<`` or ``>``, pressing the AltGr-mapped ``%`` key outputs ``=``.
    """
    return [
        ("<%", "<=", False, True, False, _needs_final_result("<="), "", False),
        (">%", ">=", False, True, False, _needs_final_result(">="), "", False),
    ]


def _equal_string_entries() -> list[tuple[str, str, bool, bool, bool, bool, str, bool]]:
    """Return corrected entries for the EqualString roll: [) ➜ = "".

    The AHK source uses consecutive backtick-escaped double-quotes
    (`` `"`" ``) followed by a cursor-movement command.  This pattern
    confuses the string-concatenation collapsing regex, so the automatic
    parser yields a truncated output (a bare backtick).  These hand-crafted
    entries override the broken parsed ones with the correct expansion.
    """
    return [
        (" [)", ' = ""', False, True, False, _needs_final_result(' = ""'), "", False),
        ("[)", ' = ""', False, True, False, _needs_final_result(' = ""'), "", False),
    ]


# Mapping from AHK sub-section names (any casing; normalised internally) to the
# Lua module identifier that handles them at runtime.  Each entry here is a
# section that appears in the AHK ``__Order`` list but produces no TOML entries
# because its logic lives entirely outside the keymap TOML pipeline.
# Hammerspoon reads this via ``module_sections`` in ``_index.json`` and renders
# the corresponding toggle item inline within the parent group's sub-menu.
_MODULE_SECTIONS: dict[str, str] = {
    "TextExpansionPersonalInformation": "personal_info",
}


def build_handcrafted_sections(
    content: str,
) -> dict[
    str,
    dict[
        str,
        tuple[list[tuple[str, str, bool, bool, bool, bool, str, bool]], str],
    ],
]:
    """Build the hand-crafted sections dict from the parsed AHK source.

    Some AHK sub-sections cannot be parsed automatically (``#HotIf`` context
    blocks, backtick-escaped literals, etc.).  This function constructs them
    from well-known helpers and from data parsed directly out of *content*.

    Args:
            content: Full text of the AHK source file.

    Returns:
            Nested dict  ``{AHK_category: {sub_name: (entries, description)}}``.
    """
    circumflex_map = parse_ahk_circumflex_map(content)
    return {
        "Rolls": {
            # Comment uses backtick-escaped double-quotes in AHK — the
            # unresolvable-pattern filter drops them during normal parsing.
            "Comment": (
                _comment_entries(),
                '\\" \u279c /* et "\\ \u279c */',
            ),
            # HashtagQuote and ChevronEqual are #HotIf blocks in AHK — they
            # use GetLastSentCharacterAt() context logic and are never emitted
            # by a CreateHotstring() call, so the parser misses them entirely.
            "HashtagQuote": (
                _hashtag_quote_entries(),
                '(# \u279c (" et [# \u279c ["',
            ),
            "ChevronEqual": (
                _chevron_equal_entries(),
                "<% \u279c <= et >% \u279c >=",
            ),
            # EqualString: AHK source concatenates two backtick-escaped
            # double-quotes + {Left}; the concat-collapsing regex yields a
            # truncated output.  Correct expansion is = "".
            "EqualString": (
                _equal_string_entries(),
                '[ ) \u279c = ""',
            ),
        },
        "DistancesReduction": {
            # Key must match the AHK __Order entry exactly so the normalised
            # name ("deadkeyecircumflex") lands in the right TOML position.
            # Description is None so parse_ahk_descriptions() value is kept.
            "DeadKeyECircumflex": (
                _deadkey_ecircumflex_entries(circumflex_map),
                None,
            ),
        },
    }


def main(
    ahk_file: Path,
) -> None:
    """Extract all AHK hotstring blocks and write one TOML file per category."""
    reset_error_count()

    logger.info("=" * 80)
    logger.info("AutoHotkey → TOML hotstring generator")
    logger.info("=" * 80)

    content = ahk_file.read_text(encoding="utf-8")
    all_blocks = find_all_blocks(content)
    logger.info(
        "Found %d categories: %s",
        len(all_blocks),
        sorted(all_blocks.keys()),
    )

    # Parse description map once, shared across all categories.
    all_descriptions = parse_ahk_descriptions(content)
    logger.info(
        "Extracted descriptions for %d categories",
        len(all_descriptions),
    )

    all_orders = parse_ahk_orders(content)
    logger.info(
        "Extracted __Order for %d categories: %s",
        len(all_orders),
        sorted(all_orders.keys()),
    )

    # --- Determine global category order from AHK top-level __Order --------
    top_level_order = parse_ahk_top_level_order(content)
    logger.info(
        "Top-level __Order: %s",
        top_level_order,
    )

    handcrafted = build_handcrafted_sections(content)

    # Sub-sections that must preserve identity hotstrings (trigger == output)
    # because they act as guards preventing shorter triggers from firing inside
    # longer words (e.g. ``honnê`` blocks ``nnê → nnu`` inside ``honnête``).
    _IDENTITY_SECTIONS: frozenset[str] = frozenset({"Repeat"})

    total_entries = 0
    for category, subs in all_blocks.items():
        sub_entries: dict[
            str, list[tuple[str, str, bool, bool, bool, bool, str, bool]]
        ] = {
            sub_name: parse_body(
                body,
                skip_identity=sub_name not in _IDENTITY_SECTIONS,
            )
            for sub_name, body in subs.items()
        }
        # Inject hand-crafted sections that belong to this category.
        for sub_name, (entries, section_desc) in handcrafted.get(
            category, {}
        ).items():
            sub_entries[sub_name] = entries
            # Only override the AHK-parsed description when the hand-crafted
            # section explicitly provides one (i.e. description is not None).
            if section_desc is not None:
                all_descriptions.setdefault(category, {})[sub_name] = (
                    section_desc
                )
        total_entries += write_category_toml(
            category,
            sub_entries,
            section_descriptions=all_descriptions.get(category),
            section_order=all_orders.get(category),
        )

    # --- Write _index.json so Hammerspoon can restore the AHK menu order ----
    # Keep only names whose TOML file was actually written (non-empty categories).
    # Exclude private categories: their TOML files are gitignored and must not
    # appear in the committed _index.json.
    written_names = {p.stem for p in OUTPUT_DIR.glob("*.toml")}
    ordered: list[str] = [
        name
        for name in top_level_order
        if name in written_names and name not in _PRIVATE_CATEGORIES
    ]
    # Append any written TOML not listed in top_level_order, alphabetically.
    for name in sorted(written_names - set(ordered) - _PRIVATE_CATEGORIES):
        ordered.append(name)

    # Build module_sections: tell Lua which placeholder sub-sections (those
    # absent from TOML because handled by a separate Lua module) map to which
    # Lua module identifier.  Keyed by normalised category / section name.
    # Each value is a dict with at least ``mod_id``; ``description`` is added
    # when an AHK description is available for the sub-section.
    _module_secs_norm: dict[str, str] = {
        normalize_name(k): v for k, v in _MODULE_SECTIONS.items()
    }
    module_sections: dict[str, dict[str, dict[str, str]]] = {}
    for category, subs_order in all_orders.items():
        normalized_cat = normalize_name(category)
        for item in subs_order:
            if item == "-":
                continue
            module = _module_secs_norm.get(item)
            if module:
                entry: dict[str, str] = {"mod_id": module}
                # Look up the AHK-parsed description for this sub-section.
                original_name = next(
                    (k for k in _MODULE_SECTIONS if normalize_name(k) == item),
                    None,
                )
                if original_name:
                    desc = (
                        all_descriptions.get(category, {})
                        or all_descriptions.get(
                            next(
                                (
                                    c
                                    for c in all_descriptions
                                    if normalize_name(c) == normalized_cat
                                ),
                                "",
                            ),
                            {},
                        )
                    ).get(original_name)
                    if desc:
                        entry["description"] = desc
                module_sections.setdefault(normalized_cat, {})[item] = entry

    index_path = OUTPUT_DIR / "_index.json"
    index_path.write_text(
        json.dumps(
            {"categories_order": ordered, "module_sections": module_sections},
            ensure_ascii=False,
            indent=2,
        )
        + "\n",
        encoding="utf-8",
    )
    logger.info(
        "Written %s → categories_order: %s, module_sections: %s",
        index_path.name,
        ordered,
        module_sections,
    )

    # Remove obsolete standalone TOML files now embedded in their parent.
    for orphan_name in ("e_deadkey.toml",):
        orphan_path = OUTPUT_DIR / orphan_name
        if orphan_path.exists():
            orphan_path.unlink()
            logger.info(
                "Removed %s (entries now embedded in parent TOML)", orphan_name
            )

    errors = get_error_count()
    logger.info("=" * 80)
    if errors == 0:
        logger.success(
            "Done – %d categories processed, %d entries, no errors.",
            len(all_blocks),
            total_entries,
        )
    else:
        logger.error(
            "Done – %d categories processed, %d entries, %d error(s).",
            len(all_blocks),
            total_entries,
            errors,
        )


if __name__ == "__main__":
    # Allow a local machine override via a gitignored file.
    # Create ".local_ahk_path" next to this script and put the absolute path
    # to your private AHK file on the first line, e.g.:
    #   /Users/you/private-config/ErgoptiPlus.ahk
    local_override = Path(__file__).parent / ".local_ahk_path"
    if local_override.exists():
        ahk_path = Path(local_override.read_text(encoding="utf-8").strip())
    else:
        ahk_path = (
            Path(__file__).parent.parent / "autohotkey" / "ErgoptiPlus.ahk"
        )
    main(ahk_path)
