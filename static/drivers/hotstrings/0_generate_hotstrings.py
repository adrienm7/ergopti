def _extract_hotstring_from_line(
    line: str,
) -> tuple[Optional[str], Optional[str], bool, bool, bool]:
    """Parse one (possibly multi-line-joined) AHK call and return its fields.

    Applies :func:`preprocess_ahk_line` first so that known AHK global
    variables (``SpaceAroundSymbols``, ``ScriptInformation["MagicKey"]``) are
    resolved and adjacent string literals are merged before the regexes run.

    Args:
            line: A single logical AHK statement, with whitespace normalised.

    Returns:
            ``(trigger, output, is_word, auto_expand, case_sensitive)`` where
            *trigger* and *output* are ``None`` when no hotstring was found.
    """
    # Resolve variables and collapse string concatenations upfront.
    line = preprocess_ahk_line(line)

    case_sensitive = "CreateCaseSensitiveHotstrings" in line

    # Try from most-specific to most-generic.
    patterns: list[str] = [
        # Complex escape: backtick-quoted output
        (
            r"(?:CreateCaseSensitiveHotstrings|CreateHotstring)\s*\(\s*"
            r'"(?P<opts>[^"]*)",\s*'
            r'"(?P<trig>(?:[^"\\]|\\.)*)",\s*'
            r'"(?P<out>`"[^"]*)"'
        ),
        # Standard CreateHotstring / CreateCaseSensitiveHotstrings
        (
            r"(?:CreateCaseSensitiveHotstrings|CreateHotstring)\s*\(\s*"
            r'"(?P<opts>[^"]*)",\s*'
            r'"(?P<trig>(?:[^"\\]|\\.)*)",\s*'
            r'"(?P<out>(?:[^"\\]|\\.)*)"'
        ),
        # Hotstring() legacy format
        (
            r"Hotstrings?\s*\("
            r'(?:\s*"(?P<opts>[^"]*)",)?'
            r'\s*"(?P<trig>[^"]+?)"'
            r'\s*,\s*"(?P<out>[^"]+?)"'
            r"\s*\)"
        ),
    ]

    for pat in patterns:
        m = re.search(pat, line, re.DOTALL)
        if not m:
            continue
        opts = m.group("opts") or ""
        trigger = process_autohotkey_escapes(m.group("trig"))
        output = process_autohotkey_escapes(m.group("out"))
        auto_expand = "*" in opts
        is_word = "?" not in opts
        return trigger, output, is_word, auto_expand, case_sensitive

    return None, None, False, False, False


def _strip_ahk_inline_comment(line: str) -> str:
    """Strip an AHK inline comment (``; ...``) from *line*.

    Respects double-quoted string literals so that semicolons inside quotes
    are not treated as comment starters.  AHK backtick escapes (`` `x ``) are
    handled inside strings.

    Args:
            line: A single AHK statement line.

    Returns:
            The line with the inline comment removed, stripped of trailing
            whitespace.
    """
    in_string = False
    i = 0
    while i < len(line):
        ch = line[i]
        if ch == "`" and in_string:
            # AHK backtick escape: skip the next character unconditionally
            i += 2
            continue
        if ch == '"':
            in_string = not in_string
        elif ch == ";" and not in_string:
            return line[:i].rstrip()
        i += 1
    return line


def parse_body(
    body: str,
) -> list[tuple[str, str, bool, bool, bool]]:
    """Extract all hotstring entries from a block body.

    Section-header comments (``; === Name ===``) are ignored – every entry
    just goes into a flat list.  Duplicate triggers within the same body are
    silently dropped (first occurrence wins).

    Args:
            body: Raw body text of a ``Features[...][...]`` block.

    Returns:
            Deduplicated list of
            ``(trigger, output, is_word, auto_expand, case_sensitive)`` tuples.
    """
    entries: list[tuple[str, str, bool, bool, bool]] = []
    seen: set[str] = set()
    lines = body.split("\n")
    i = 0

    while i < len(lines):
        line = lines[i].strip()
        if not line or line.startswith(";"):
            i += 1
            continue

        # Join continuation lines for multi-line calls — also covers bare
        # ``Hotstring(`` (low-level callback form with no ``Create`` prefix).
        _is_open_call = (
            "CreateCaseSensitiveHotstrings(" in line
            or "CreateHotstring(" in line
            or bool(re.search(r"(?<![A-Za-z])Hotstring\s*\(", line))
        )
        if _is_open_call and not _strip_ahk_inline_comment(line).endswith(")"):
            j = i + 1
            while j < len(lines) and not _strip_ahk_inline_comment(
                line
            ).endswith(")"):
                next_fragment = lines[j].strip()
                # Ignore standalone comment lines; they corrupt the joined expression.
                if not next_fragment.startswith(";"):
                    line += " " + next_fragment
                j += 1
            i = j
        else:
            i += 1

        trigger, output, is_word, auto_expand, case_sensitive = (
            _extract_hotstring_from_line(line)
        )

        if trigger and output:
            # Skip identity hotstrings (trigger == output): they are no-ops in
            # Hammerspoon and only exist in AHK to block longer hotstrings.
            if trigger == output:
                pass
            elif trigger not in seen:
                seen.add(trigger)
                entries.append(
                    (trigger, output, is_word, auto_expand, case_sensitive)
                )
        elif any(
            kw in line
            for kw in (
                "CreateHotstring",
                "CreateCaseSensitiveHotstrings",
                "Hotstring",
            )
        ):
            # Silently skip patterns that are inherently unresolvable at parse
            # time (loop variables, callback lambdas, etc.).
            if not _UNRESOLVABLE_PATTERNS.search(line):
                logger.warning(
                    "Failed to parse: %s",
                    (line[:120] + "...") if len(line) > 120 else line,
                )

    return entries


