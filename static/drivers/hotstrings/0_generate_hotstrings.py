# Description extraction
# ---------------------------------------------------------------------------


def _parse_ahk_desc_expr(expr: str) -> str:
    """Resolve an AHK string-concatenation expression into a plain string.

    Replaces ``ScriptInformation["..."]`` references with ``★`` and joins all
    double-quoted fragments.

    Args:
            expr: Raw AHK expression after ``Description:``, e.g.
                    ``"text " . ScriptInformation["MagicKey"] . " suite"``.

    Returns:
            Resolved plain string, e.g. ``"text ★ suite"``.
    """
    # Split on the AHK concatenation operator ' . ' (always surrounded by spaces).
    # Using \s+ (not \s*) avoids splitting on literal dots inside quoted strings
    # (e.g. "ê. = u." or "$eq.not$").
    tokens = re.split(r"\s+\.\s+", expr.strip().rstrip(","))
    parts: list[str] = []
    for tok in tokens:
        tok = tok.strip()
        if tok.startswith('"') and tok.endswith('"'):
            # Plain quoted literal — unescape AHK backtick sequences
            parts.append(process_autohotkey_escapes(tok[1:-1]))
        elif "ScriptInformation" in tok:
            parts.append("★")
        # else: skip things like numbers, booleans, etc.
    return "".join(parts)


def parse_ahk_descriptions(content: str) -> dict[str, dict[str, str]]:
    """Extract per-section descriptions from the ``Features`` map in *content*.

    Scans the AHK source for ``"SubName", { ... Description: ... }`` blocks
    and records the resolved description text for each ``(category, sub)`` pair.

    Args:
            content: Full AHK file content.

    Returns:
            ``{AHK_category_name: {AHK_sub_name: description_string}}``
    """
    # Locate the Features map block
    features_start = content.find("global Features := Map(")
    if features_start < 0:
        return {}

    # The Features map is followed by the first top-level if/hotstring block.
    # We only scan the initial declarations section.
    first_if = re.search(
        r"^\s*if\s+Features\[", content[features_start:], re.MULTILINE
    )
    features_text = (
        content[features_start : features_start + first_if.start()]
        if first_if
        else content[features_start : features_start + 20_000]
    )

    result: dict[str, dict[str, str]] = {}

    # Regexes to track context
    cat_re = re.compile(r'"([A-Za-z]+)",\s*Map\(')
    sub_re = re.compile(r'"([^"]+)",\s*\{')
    desc_re = re.compile(r"Description:\s*(.*)")

    lines = features_text.splitlines()
    current_cat: Optional[str] = None
    current_sub: Optional[str] = None
    i = 0

    while i < len(lines):
        line = lines[i].strip()

        cm = cat_re.search(line)
        if cm and cm.group(1) != "__Order":
            current_cat = cm.group(1)
            current_sub = None

        sm = sub_re.search(line)
        if sm:
            cand = sm.group(1)
            if cand not in ("__Order",) and not cand.startswith("-"):
                current_sub = cand

        dm = desc_re.search(line)
        if dm and current_cat and current_sub:
            desc_expr = dm.group(1).strip()
            # Collect continuation lines when the expression is incomplete:
            # either it ends with the concatenation operator '.' or it ends
            # with an open bracket (e.g. ScriptInformation[ split across lines).
            while (
                desc_expr.rstrip(",").rstrip().endswith(".")
                or desc_expr.rstrip().endswith("[")
            ) and i + 1 < len(lines):
                i += 1
                desc_expr += " " + lines[i].strip()
            desc = _parse_ahk_desc_expr(desc_expr)
            if desc:
                result.setdefault(current_cat, {})[current_sub] = desc

        i += 1

    return result


