def entries_to_toml_lines(
    entries: list[tuple[str, str, bool, bool, bool, bool, str, bool]],
) -> list[str]:
    """Render a list of hotstring entries as TOML assignment lines.

    Each entry is an 8-tuple ``(trigger, output, is_word, auto_expand,
    case_sensitive, final_result, comment, is_disabled)``.  The
    ``final_result`` field is always written (``true`` or ``false``) so that
    Hammerspoon can rely on its presence without a default-value fallback.
    A non-empty *comment* is appended as an inline ``# …`` note and
    *is_disabled* entries are prefixed with ``# `` so the whole TOML
    assignment becomes a comment (mirroring an AHK ``; CreateHotstring(...)``
    line).

    Args:
            entries: Hotstring tuples produced by :func:`parse_body`.

    Returns:
            One TOML line per entry.
    """
    lines = []
    for row in entries:
        trigger, output, is_word, auto_expand, case_sensitive = row[:5]
        final_result: bool = row[5] if len(row) > 5 else False  # type: ignore[index]
        comment: str = row[6] if len(row) > 6 else ""  # type: ignore[index]
        is_disabled: bool = row[7] if len(row) > 7 else False  # type: ignore[index]
        strict_case: bool = row[8] if len(row) > 8 else False  # type: ignore[index]
        t = escape_toml_string(trigger)
        o = escape_toml_string(output)
        line = (
            f'"{t}" = {{ output = "{o}"'
            f", is_word = {str(is_word).lower()}"
            f", auto_expand = {str(auto_expand).lower()}"
            f", is_case_sensitive = {str(not case_sensitive).lower()}"
            f", final_result = {str(final_result).lower()}"
        )
        # Emit the optional ``is_case_sensitive_strict`` flag only when set so
        # the common case (default ``false``) stays untouched and TOML diffs
        # remain minimal. The AHK loader treats a missing field as ``false``.
        if strict_case:
            line += ", is_case_sensitive_strict = true"
        line += " }"
        if comment:
            line += f"  # {comment}"
        if is_disabled:
            line = "# " + line
        lines.append(line)
    return lines


def write_category_toml(
    category: str,
    sub_blocks: dict[
        str, list[tuple[str, str, bool, bool, bool, bool, str, bool]]
    ],
    section_descriptions: Optional[dict[str, str]] = None,
    section_order: Optional[list[str]] = None,
) -> int:
    """Write one TOML file for *category* and return the total entry count.

    The filename is :func:`normalize_name` applied to *category*.
    Each sub-block becomes a ``[[sub_name]]`` array-of-tables section.
    A leading ``[_meta]`` table carries the human-readable descriptions used
    by the Hammerspoon menu, including an optional ``sections_order`` array
    that records the original AHK ``__Order`` list for faithful menu ordering.

    Args:
            category: Original AHK category name (e.g. ``"MagicKey"``).
            sub_blocks: Mapping of sub-block names to their hotstring entries.
            section_descriptions: Optional ``{AHK_sub_name: description}`` mapping
                    extracted from the AHK Features map.
            section_order: Optional ordered list of *normalised* section names
                    (with ``"-"`` as separator sentinel) extracted from the AHK
                    ``__Order`` array.  Sections not listed are appended at the
                    end.  When ``None``, the insertion order of *sub_blocks* is
                    kept.

    Returns:
            Total number of entries written.
    """
    filename = normalize_name(category) + ".toml"
    output_file = OUTPUT_DIR / filename

    lines = get_toml_header()

    # ── Build final section order ─────────────────────────────────────────────
    known_subs = list(sub_blocks.keys())
    norm_known = [normalize_name(s) for s in known_subs]

    if section_order:
        ordered: list[str] = []
        seen_norm: set[str] = set()
        for item in section_order:
            if item == "-":
                ordered.append("-")
            elif item in norm_known and item not in seen_norm:
                ordered.append(item)
                seen_norm.add(item)
        # Append any sub that was not referenced in __Order
        for norm in norm_known:
            if norm not in seen_norm:
                ordered.append(norm)
                seen_norm.add(norm)
    else:
        ordered = norm_known

    # ── [_meta] block ────────────────────────────────────────────────────────
    file_desc = FILE_DESCRIPTIONS.get(category, "")
    lines.append("[_meta]")
    lines.append(f'description = "{escape_toml_string(file_desc)}"')

    # Write sections_order as a TOML inline array (preserves order + separators)
    toml_items = ", ".join(f'"{item}"' for item in ordered)
    lines.append(f"sections_order = [{toml_items}]")
    lines.append("")

    if section_descriptions:
        lines.append("[_meta.sections]")
        for sub_name in sub_blocks:
            norm = normalize_name(sub_name)
            desc = section_descriptions.get(sub_name, "")
            if desc:
                lines.append(f'{norm} = "{escape_toml_string(desc)}"')
        lines.append("")

    # ── section data — written in the resolved order ──────────────────────────
    norm_to_orig = {normalize_name(s): s for s in known_subs}
    total = 0
    for item in ordered:
        if item == "-":
            continue  # separators have no data block
        orig = norm_to_orig.get(item)
        if orig is None:
            continue
        entries = sub_blocks.get(orig, [])
        if not entries:
            continue
        lines.append(f"[[{item}]]")
        lines.extend(entries_to_toml_lines(entries))
        lines.append("")
        total += len(entries)

    if total == 0:
        logger.warning("Skipping %s – no entries found", filename)
        return 0

    # Nouvelle logique : comparer le contenu généré (hors date) à l'existant
    def strip_date(toml: str) -> str:
        return "\n".join(
            [
                line
                for line in toml.splitlines()
                if not line.startswith(
                    "# This file was automatically generated by 0_generate_hotstrings.py on "
                )
            ]
        )

    new_content = "\n".join(lines)
    if output_file.exists():
        existing_content = output_file.read_text(encoding="utf-8")
        if (
            strip_date(new_content).strip()
            == strip_date(existing_content).strip()
        ):
            # Pas de changement hors date, ne pas écraser
            logger.info("Unchanged %s (only date would differ)", filename)
            return total

    output_file.write_text(new_content, encoding="utf-8")
    logger.info("Written %s (%d entries)", filename, total)
    return total


