<h2>Alternance des mains</h2>
<p>
	L’alternance des mains est très importante pour garantir une bonne <span class="important"
		>fluidité de frappe</span
	>, et donc un meilleur confort. L’objectif est d’essayer d’avoir le plus possible d’alternance des
	mains lors de la frappe du texte : main droite, puis main gauche, main droite, main gauche, etc.
</p>
<p>
	Cette alternance des mains permet de <span class="important"
		>ne pas surutiliser l’une des deux mains</span
	> en n’en utilisant qu’une pour taper la majorité du texte. Cela se ferait en effet au détriment de
	l’autre main qui resterait au-dessus de sa partie du clavier, à attendre de pouvoir enfin entrer en
	jeu. Ainsi, avec une bonne alternance des mains, pendant qu’une main frappe une touche, l’autre peut
	se replacer sur la rangée du milieu et se préparer à frapper la suivante.
</p>
<p>
	Si la touche suivante est sur la même main que la touche précédente (sauf éventuellement avec un
	roulement, cf. la partie suivante), la frappe sera moins confortable. Imaginez que vous deviez
	atteindre le <kbd>C</kbd>
	de l’AZERTY, puis le <kbd>R</kbd>, pour écrire <kbd>CR</kbd>, un bigramme très courant en français
	et anglais. Dans ce cas, vous devez d’abord légèrement abaisser votre main pour que le majeur
	atteigne la rangée du bas pour taper
	<kbd>C</kbd>. Puis, il faut que l’index atteigne quant à lui la rangée du haut pour taper
	<kbd>R</kbd>. En résulte un sentiment d’inconfort avec deux doigts proches mais qui doivent aller
	dans des directions différentes. Le terme technique pour un enchaînement de ce type est appelé
	<em>ciseau</em>, car les doigts font un grand écart pour atteindre des rangées complètement
	différentes. Au contraire,
	<span class="important">en "parallélisant" les frappes sur les deux mains</span>, le résultat se
	révèle bien plus satisfaisant et ces ciseaux sont évités.
</p>
<p>
	L’alternance des mains a une limite. Il est impossible d’alterner parfaitement à chaque frappe.
	C’est pourquoi, une fois une alternance maximale atteinte, il faut encore améliorer l’expérience
	de frappe dans le cas où deux touches de la même main doivent être frappées d’affilée. C’est là
	qu’interviennent les roulements, présentés en détail dans le point suivant.
</p>
